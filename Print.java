public class Print {

        public static void main(String[] args) {
        
        System.out.print("I like ice cream!");
        System.out.print("It's really good!");
        System.out.print("Buy me ice cream!");
        // without ln or \n it will print on the same line

        System.out.println();
        System.out.println("I like ice cream!");
        System.out.print("It's really good!\n");
        System.out.println("Buy me ice cream!");
        // with ln or /n it will print on a new line

        System.out.println();
        /*  
        type "sout" then press tab to print a new line 
        */
    }    
    
}